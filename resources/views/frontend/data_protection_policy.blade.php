                   <p>This policy explains how the  University of Calabar Alumni handles and uses the 
                    personal data we collect about our alumni and stakeholders including attendees at 
                    events organized by Unical Alumni.</p>
                    <p>The purpose of developing this e-newsletter is to develop a robust and better 
                        relationship with Alumnus by keeping in touch with you, to provide services 
                        to you, and to identify ways in which you can support. </p>
                        <p>We are committed to protecting your personal information and being transparent
                         about what information we hold. The following statement sets out how Unical Alumni
                           Specifically uses your personal data for alumni relations purposes</p>
                          <h5> Personal Data We Hold</h5>
<p>We may hold information relating to you from a number of sources. Most of the information we hold on 
    alumni is that which you provided to us by filling our Google consent form or by corresponding with
     us by telephone, email or transferred from your student record to the University alumni database. <p>
   <h5>  Most records contain:</h5>
<p>Names, Details of your Education,Student number, Date of birth,Contact details,Course of study completion dates 
,Marital status, the name of partner and spouse,Disability status ,Communication preferences</p>
<h5>We also in some cases have your information on:</h5>
<ul>
    <li>The history of your donations to the University</li>

<li>Your ability and willingness to make donations, 
    and whether a particular donations or funding appeals may be of interest to you</li>
<li>Your career highlights and other life achievements</li>
<li>Information about your interest and extra-curricular activities</li>
</ul>
<h5>How we use your data:</h5>
<p> Your data is used for alumni relations communications and fundraising.  These include:</p>
<ul>
    <li>Sending you publications like e-newsletters, alumni magazines and updates about University of 
        Calabar</li> 
    <li>Conducting surveys, including research on when and whether particular donations or funding 
        appeals may be of interest to you</li> 
    <li>Providing services.....list facilities, alumni emails accounts etc, admissions etc</li> 
    <li>Sending you tailored proposals, appeals and requests for donations</li> 
    <li>Sending you details of volunteering opportunities</li> 
    <li>Inviting you to alumni and other University events</li> 
    <li>The promotion of opportunities and services  </li> 
    <li>Analysis and research to improve our understanding of our alumni to inform 
        fundraising strategy and communications</li> 
    <li>Manage any feedback or complaints</li> 
    <li>Administrative purposes  to process a donation you have
         made or to administer an event you have registered </li> 

</ul>
<p>Communications to you may be sent by post, telephone or electronic means (principally by email), 
    depending on the contact details we hold, the consent that you have provided, and the preferences 
    expressed by you about the types of communications you wish to receive. If you have concerns or 
    queries about any of these purposes, or how we communicate with you, please contact us using the 
    details listed below.</p>
    <p>We may use automated or manual analyses to link data together to help us identify your engagement 
        with Alumni and the University, to provide you with an improved experience, to send you relevant
         and timely communications, to identify opportunities of your interest, and to avoid approaching
          you with opportunities which are not of interest. We always seek to ensure that any 
          opportunities we present align with your interests, based on the research we conduct.</p>
<p>We will always respect a request by you to stop processing your personal data, and in addition your 
    statutory rights are set out below.</p>
  <h5>  When we share your data with others (our partners)</h5>
<p>We share your data with the University of Calabar to provide alumni with a coordinated engagement.
      The University have access to our database, and we maintain other electronic and paper 
    records.  Any transmission of data to or from the University is managed through agreed processes
    . </p>
    <p>We share data on a considered and confidential basis, where appropriate, with affiliated 
        organisations and individuals which support and provide services to alumni such as</p>
        <ul>
            <li>Alumni  for which we help manage events that you attend</li>
            <li>Partners, Clubs, Societies and charities with close connections to the University for
                 which we process your donation, or which we believe you may wish to support</li>
            <li>Advisory board members  and volunteers supporting events organise by Unical Alumn</li>
            <li>Educational trusts associated with the University that awarded you a scholarship</li>
        </ul>
        <p>
   Third parties engaged by the University to provide related services, such as:</p>
   <ul>
    <li>Companies that provide us with data about alumni and supporters</li>
    <li>Consultants advising us on individuals’ capacity to donate</li>
    <li>Contractors providing services to you on our behalf or services to us</li>
    <li>Companies who provide University-branded or University-endorsed products and services</li>
   </ul>
  <h5> How we protect your data</h5>
<p>We ensure we have appropriate data sharing agreements in place before sharing your personal data. 
    We cannot sell your personal data to third parties under any circumstances. We also facilitate 
    communication between individual alumni, but in doing so we do not release personal contact details
     without prior permission. Further information about the measures we use to protect data when being
      transferred internationally is available from our link</p>
      <h5>Your rights: You have the right to:</h5>
      <ul>
        <li>Ask us for access to, or rectification or erasure of your data</li>
        <li>Restrict processing (pending correction or deletion)</li>
        <li>Object to communications or direct marketing</li>
        <li>Ask for the transfer of your data electronically to a third party (data portability)</li>
        <li>Many of these rights are qualified in various ways.</li>
      </ul>
